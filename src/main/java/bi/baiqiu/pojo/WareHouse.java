    /**
     * 今日预售发货量
     */
    private Integer todayShippedPresellAmount = 0;

    /**
     * 上月发货
     */
    private Integer lastMonthShippedAmount = 0;

    /**
     * 本月发货
     */
    private Integer presentMonthShippedAmount = 0;

    /**
     * 数据修改时间
     */
    private Date modifyTime;

    
    
    public WareHouse() {
		super();
	}

