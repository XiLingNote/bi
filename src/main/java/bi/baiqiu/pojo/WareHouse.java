    public void setTodayShippedPresellAmount(Integer todayShippedPresellAmount) {
        this.todayShippedPresellAmount = todayShippedPresellAmount;
    }

    public Integer getLastMonthShippedAmount() {
        return lastMonthShippedAmount;
    }

    public void setLastMonthShippedAmount(Integer lastMonthShippedAmount) {
        this.lastMonthShippedAmount = lastMonthShippedAmount;
    }

    public Integer getPresentMonthShippedAmount() {
        return presentMonthShippedAmount;
    }

    public void setPresentMonthShippedAmount(Integer presentMonthShippedAmount) {
        this.presentMonthShippedAmount = presentMonthShippedAmount;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

